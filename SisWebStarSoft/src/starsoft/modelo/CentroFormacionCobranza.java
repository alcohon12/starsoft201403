package starsoft.modelo;

public class CentroFormacionCobranza {

	private String centro_formacion;
	private String tipo;
	private Double monto;
	private int nro_Ideas;
	private Double Pago;
	
	
	public String getCentro_formacion() {
		return centro_formacion;
	}
	public void setCentro_formacion(String centro_formacion) {
		this.centro_formacion = centro_formacion;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public Double getMonto() {
		return monto;
	}
	public void setMonto(Double monto) {
		this.monto = monto;
	}
	public int getNro_Ideas() {
		return nro_Ideas;
	}
	public void setNro_Ideas(int nro_Ideas) {
		this.nro_Ideas = nro_Ideas;
	}
	public Double getPago() {
		return Pago;
	}
	public void setPago(Double pago) {
		Pago = pago;
	}
	

}
